#include	"ft_printf.h"

void	ft_putnbr(int n, int *len)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		(*len) += 11;
		return (0);
	}
	if (n < 0)
	{
		ft_putchar_len('-', len);
		ft_putnbr(-n, len);
	}
	else
	{
		if (n > 9)
			ft_putnbr(n / 10, len);
		ft_putchar_len(n % 10 + '0', len);
	}
}
